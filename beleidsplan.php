    <h2>Beleidsplan Stichting Buitenbeesten</h2>
<div  id="beleidsplanleft">

    <h3>MISSIE</h3>
    <p>Onze missie bestaat uit het investeren in kwetsbare kinderen (die over het algemeen niet in aanmerking komen voor regulier onderwijs),  door deelname aan individuele of groepsbegeleiding gebruikmakend van dieren (‘buitenbeesten’) en paarden c.q. pony’s in het bijzonder.</p>

    <h3>VISIE</h3>
    <p>Wij werken met een lange termijndoel, met de inzet van dieren en de daarbij behorende omgeving, aan de toekomst van meer kwetsbare kinderen;  deze kinderen te kunnen laten groeien in hun mogelijkheden; zodat zij met meer vaardigheden zelfstandiger en met meer zelfvertrouwen verder kunnen ontwikkelen.</p>

    <h3>DOELSTELLINGEN</h3>
    <p>Kernactiviteit is het meer zelfstandig maken van kinderen, in een leeromgeving met dieren, paarden c.q. pony’s in het bijzonder, o.l.v. gekwalificeerde collega’s.</p>

    <h3>DOELGROEP</h3>
    <p>Kinderen in de leeftijd van 4 tot ca. 16 jaar. Het betreft vrijwel uitsluitend kinderen die niet in aanmerking komen voor regulier onderwijs.</p>

    <h3>ACTIVITEITEN</h3>
<p>Afhankelijk van de hulpvraag van het kind bieden wij zowel individuele als (kleine) groepsbegeleiding.
    Er worden individuele begeleidingsplannen gemaakt.</p>
    </div>

    
    <div id="beleidsplanright">
