    <h3 id="orgtext">ORGANISATIE</h3>
<p id="orgopening">Het bestuur bestaat uit 3 bestuursleden, te weten een voorzitter, secretaris en een penningmeester.
    Er wordt uitgegaan van een toezichthoudend bestuur.</p>

    <div class="wrapper"><p><strong>Administratieve verplichting</strong></p>
    <p>Bij het verkrijgen van de ANBI-status, zal er aan de daarbij behorende verplichtingen worden voldaan.</p>    </div>

    <div class="wrapper"><p><strong>Groeistrategie</strong></p>
        <p>Momenteel zijn er ca. 30 kinderen verbonden aan Buitenbeesten; maximaal 35 kinderen kunnen geplaatst worden, dit om kwaliteit te kunnen waarborgen. Uitbreiding zou meer organisatie verlangen en afleiden van hetgeen waar we mee bezig willen zijn.</p></div>

     <div class="wrapper"><p><strong>Fondswerving</strong></p>
         <p>Wordt gedaan d.m.v. alle binnen het kader van haar werkzaamheden aanvaardbare inzamelingstechnieken.</p></div>

       <div class="lastchild">  <p><strong>Bestedingen</strong></p>
<p>Aanschaf/vernieuwen of reparatie van materiaal
Huur locatie
    Inhuur gekwalificeerd personeel, zij factureren direct aan de stichting

Bestuursleden ontvangen geen vergoedingen.

De ANBI status verleent de particuliere donateur de mogelijkheid in de aangifte inkomstenbelasting, het gedoneerde bedrag 1,25 keer van de gift aften trekken.
    Voor ondernemingen geldt hetzelfde maar dan 1,5 keer in de aangifte VPB.</div>
    
        </p>
        </div>
        <h3>EPILOOG</h3>
        <p>Als non-profit organisatie, zet Buitenbeesten zich in voor kinderen die niet goed mee kunnen komen in het regulier onderwijs en vrijetijdsbestedingen. Met gekwalificeerde collega’s, waaronder kinderfysiotherapeuten, gebruiken wij onze kennis en vaardigheden om samen met dieren en hun leefomgeving, kinderen te begeleiden in hun ontwikkeling op het gebied van zowel sociaal, fysiek, spraak/taal en sensorische integratie uitdagingen.</p>
